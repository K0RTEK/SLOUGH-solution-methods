import numpy as np

# Определение размерности СЛАУ
n = 3

# Определение матрицы коэффициентов размерности n x n
A = np.array([[2, 1, -1],
              [3, 2, 1],
              [1, -1, 2]], dtype=float)

# Определение вектора свободных членов размерности n
b = np.array([4, 2, 3], dtype=float)

# Приведение системы к треугольному виду с помощью метода Гаусса
for k in range(n):
    # Поиск максимального элемента в столбце
    max_row = np.argmax(np.abs(A[k:, k])) + k
    
    # Перестановка строк
    A[[k, max_row]] = A[[max_row, k]]
    b[[k, max_row]] = b[[max_row, k]]
    
    # Деление строки на главный элемент
    A[k] /= A[k, k]
    b[k] /= A[k, k]
    
    # Вычитание текущей строки из следующих строк
    for i in range(k + 1, n):
        factor = A[i, k]
        A[i] -= factor * A[k]
        b[i] -= factor * b[k]

# Обратный ход метода Гаусса
x = np.zeros(n)
x[n - 1] = b[n - 1]
for k in range(n - 2, -1, -1):
    x[k] = b[k] - np.dot(A[k, k + 1:], x[k + 1:])


# Вывод решения
print("Метод Гаусса: метод единичного деления:")
print(x)

